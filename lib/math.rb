def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  total = num1 - num2
  total
end

def division(num1, num2)
  answer = num1 / num2
  answer
end

def multiplication(num1, num2)
  result = num1 * num2
end

def modulo(num1, num2)
  remainder = num1 % num2
end

def square_root(num)
  Math.sqrt(num)
end

